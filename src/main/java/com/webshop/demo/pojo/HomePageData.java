package com.webshop.demo.pojo;

public class HomePageData {
    private String productName;
    private String selectedItem;
    private String typedProductName;

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getSelectedItem() {
        return selectedItem;
    }

    public void setSelectedItem(String selectedItem) {
        this.selectedItem = selectedItem;
    }

    public String getTypedProductName() {
        return typedProductName;
    }

    public void setTypedProductName(String typedProductName) {
        this.typedProductName = typedProductName;
    }
}
